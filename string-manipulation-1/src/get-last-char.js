/* GOAL: identify the LAST character of each string and return its value.
1. Create a function with one parameter, string (GIVEN)
2. Find the last letter of any given JS string (How? Use a method.)
   return string.length - 1
3. Return the LAST character of the string.

*/

function getLastChar(string) {
  return string[string.length - 1];
}
